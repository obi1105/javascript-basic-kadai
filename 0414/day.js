let day = '月'

switch (day) {
    case '月':
        console.log('週の始まりです！');
        break;
    case '火':
        console.log('まだまだこれからですよ');
        break;
    case '水':
        console.log('折り返し地点ですね');
        break;
    case '木':
        console.log('残り２日です！');
        break;
    case '金':
        console.log('あと少しです！');
        break;
    case '土':
        console.log('休日ですね');
        break;
    case '日':
        console.log('ゆっくりしましょう');
        break;
}